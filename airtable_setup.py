# ── Airtable API Client ───────────────────────────────────────────────────────

class AirtableSetup:
    """
    Connects to the Airtable Metadata API and creates the full field schema
    for the Marketing Partnership Signals table.
    """

    META_BASE_URL = "https://api.airtable.com/v0/meta"

    def __init__(self):
        self.api_token = os.environ.get('AIRTABLE_API_TOKEN', '').strip()
        self.base_id   = os.environ.get('AIRTABLE_BASE_ID', '').strip()

        if not self.api_token or self.api_token in ('your_token_here', ''):
            self._missing_credentials()

        if not self.base_id or self.base_id in ('your_base_id_here', ''):
            self._missing_credentials()

        self.headers = {
            'Authorization': f'Bearer {self.api_token}',
            'Content-Type': 'application/json'
        }

    # ── Credential helper ──────────────────────────────────────────────────────

    @staticmethod
    def _missing_credentials():
        print("\n❌  Missing Airtable credentials.\n")
        print("    Add the following to your .env file:\n")
        print("        AIRTABLE_API_TOKEN=pat_xxxxxxxxxx")
        print("        AIRTABLE_BASE_ID=appXXXXXXXXXXXXXX\n")
        print("    Get your token at:  https://airtable.com/create/tokens")
        print("    Find your Base ID:  open the base → Help → API docs → base ID in the URL\n")
        sys.exit(1)

    # ── Table Discovery ────────────────────────────────────────────────────────

    def get_tables(self) -> List[Dict]:
        """Fetch all tables in the configured base."""
        url = f"{self.META_BASE_URL}/bases/{self.base_id}/tables"
        response = requests.get(url, headers=self.headers)

        if response.status_code == 401:
            print("❌  Authentication failed. Check your AIRTABLE_API_TOKEN.")
            sys.exit(1)
        if response.status_code == 404:
            print(f"❌  Base '{self.base_id}' not found. Check your AIRTABLE_BASE_ID.")
            sys.exit(1)
        if response.status_code != 200:
            print(f"❌  Failed to fetch tables: {response.status_code} — {response.text}")
            sys.exit(1)

        return response.json().get('tables', [])

    def find_table(self, table_name: str) -> Optional[Dict]:
        """Find a table by exact name."""
        for table in self.get_tables():
            if table.get('name') == table_name:
                return table
        return None

    @staticmethod
    def get_existing_field_names(table: Dict) -> List[str]:
        """Return a list of field names that already exist in the table."""
        return [field['name'] for field in table.get('fields', [])]

    # ── Field Creation ────────────────────────────────────────────────────────

    def _build_payload(self, field_def: Dict) -> Dict:
        """Convert our field definition into an Airtable API-compatible payload."""
        field_type = field_def['type']

        payload: Dict[str, Any] = {
            'name': field_def['name'],
            'type': field_type,
        }

        if 'description' in field_def:
            payload['description'] = field_def['description']

        # singleSelect → wrap choices in options
        if field_type == 'singleSelect':
            payload['options'] = {
                'choices': field_def.get('options', [])
            }

        # currency, number, date, dateTime, checkbox → pass options as-is
        elif 'options' in field_def:
            payload['options'] = field_def['options']

        return payload

    def create_field(self, table_id: str, field_def: Dict, dry_run: bool = False) -> bool:
        """
        Create a single field via the Airtable Metadata API.

        Returns True on success or skip, False on error.
        """
        field_name = field_def['name']

        if dry_run:
            print(f"   [DRY RUN] Would create: {field_name} ({field_def['type']})")
            return True

        url = f"{self.META_BASE_URL}/bases/{self.base_id}/tables/{table_id}/fields"
        payload = self._build_payload(field_def)

        response = requests.post(url, headers=self.headers, json=payload)

        # Respect Airtable rate limits (5 req/sec for metadata API)
        time.sleep(0.25)

        if response.status_code in (200, 201):
            print(f"   ✅  Created : {field_name}  ({field_def['type']})")
            return True

        error_data = response.json() if response.text else {}
        error_msg  = error_data.get('error', {}).get('message', response.text)

        if response.status_code == 422:
            # Typically "field already exists" or invalid config
            print(f"   ⚠️   Skipped : {field_name} — {error_msg}")
            return True   # Don't count as failure

        print(f"   ❌  Failed  : {field_name} — {response.status_code} {error_msg}")
        return False

    # ── Main Setup Flow ───────────────────────────────────────────────────────

    def setup(self, table_name: str = "Marketing Partnership Signals", dry_run: bool = False):
        """
        Run the full setup: find the table, diff existing fields, create missing ones.
        """
        print("\n🎯  TTR Signal Detection — Airtable Field Setup")
        print("=" * 60)

        if dry_run:
            print("⚠️   DRY RUN MODE — no changes will be made\n")

        # Find table
        print(f"🔍  Looking for table: '{table_name}' in base {self.base_id}...")
        table = self.find_table(table_name)

        if not table:
            print(f"\n❌  Table '{table_name}' not found.")
            print("\n📋  Available tables in this base:")
            for t in self.get_tables():
                print(f"    • {t['name']}  (ID: {t['id']})")
            print(f"\n    Use --table \"<exact name>\" to target a different table.")
            sys.exit(1)

        table_id = table['id']
        print(f"✅  Found table: '{table_name}'  (ID: {table_id})\n")

        # Diff existing vs required
        existing = self.get_existing_field_names(table)
        to_create = [f for f in FIELD_DEFINITIONS if f['name'] not in existing]
        to_skip   = [f for f in FIELD_DEFINITIONS if f['name'] in existing]

        print(f"📊  Field summary:")
        print(f"    • Total fields required : {len(FIELD_DEFINITIONS)}")
        print(f"    • Already exist         : {len(to_skip)}")
        print(f"    • Will be created       : {len(to_create)}")

        if to_skip:
            print(f"\n⏭️   Skipping existing fields:")
            for f in to_skip:
                print(f"    • {f['name']}")

        if not to_create:
            print("\n✅  All fields already exist — nothing to do!\n")
            return

        print(f"\n🔨  Creating {len(to_create)} fields...\n")

        success_count = 0
        fail_count    = 0

        for field_def in to_create:
            ok = self.create_field(table_id, field_def, dry_run)
            if ok:
                success_count += 1
            else:
                fail_count += 1

        # ── Summary ──────────────────────────────────────────────────────────
        print(f"\n{'='*60}")
        print(f"📊  Results:")
        print(f"    • Created successfully : {success_count}")
        print(f"    • Failed               : {fail_count}")
        print(f"    • Already existed      : {len(to_skip)}")

        if fail_count == 0:
            print(f"\n🎉  Setup complete! All fields are ready in '{table_name}'.")
            print(f"\n📍  Next steps:")
            print(f"    1. Run: python3 run_signal_detection.py --days=1")
            print(f"    2. Check that signals appear in your Airtable table")
            print(f"    3. Review signals and set 'Review Status' → 'Approved'")
            print(f"    4. Check 'Send to Attio' to queue records for CRM sync")
        else:
            print(f"\n⚠️   {fail_count} field(s) failed. Review errors above and re-run the script.")
            print(f"    The script is safe to re-run — existing fields are never overwritten.\n")


