# ── Entry Point ───────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description='TTR Airtable Setup — create field schema in an existing table'
    )
    parser.add_argument(
        '--table',
        default='Marketing Partnership Signals',
        help='Exact table name to set up (default: "Marketing Partnership Signals")'
    )
    parser.add_argument(
        '--dry-run',
        action='store_true',
        help='Preview what would be created without making any changes'
    )
    parser.add_argument(
        '--list-fields',
        action='store_true',
        help='Print the full field structure (no credentials required)'
    )
    parser.add_argument(
        '--json',
        action='store_true',
        help='Output field structure as JSON (no credentials required)'
    )

    args = parser.parse_args()

    # ── No-credential modes ───────────────────────────────────────────────────
    if args.list_fields:
        print_field_structure()
        return

    if args.json:
        print(json.dumps(FIELD_DEFINITIONS, indent=2))
        return

    # ── Live API mode ─────────────────────────────────────────────────────────
    setup = AirtableSetup()
    setup.setup(table_name=args.table, dry_run=args.dry_run)


if __name__ == "__main__":
    main()
